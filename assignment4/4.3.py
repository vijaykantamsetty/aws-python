name = "some name"

upper_name = name.upper()
print(upper_name)  # "SOME NAME"

lower_name = name.lower()
print(lower_name)  # "some name"

capitalized_name = name.capitalize()
print(capitalized_name)  # "Some name"

replaced_name = name.replace('e', 'E')
print(replaced_name)  # "somE namE"