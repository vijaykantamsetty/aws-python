# Using + operator
str1 = "Hello"
str2 = "World"
result = str1 + " " + str2
print(result)  # "Hello World"

# Using join() method
words = ["Hello", "World"]
result = " ".join(words)
print(result)  # "Hello World"

# Using formatted string literals (f-strings)
str1 = "Hello"
str2 = "World"
result = f"{str1} {str2}"
print(result)  # "Hello World"